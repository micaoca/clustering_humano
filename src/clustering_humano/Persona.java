package clustering_humano;

public class Persona  {
	
	private String nombre;
	private int d;
	private int m;
	private int e;
	private int c;
	
	
	public Persona(String nombre, int d, int m, int e, int c) {
		this.nombre = nombre;
		this.d = d;
		this.m = m;
		this.e = e;
		this.c = c;
		
	}

	public String getNombre() {
		return nombre;
	}
	
	public int getD() {
		return d;
	}

	public int getM() {
		return m;
	}

	public int getE() {
		return e;
	}

	public int getC() {
		return c;
	}


	
	

}
